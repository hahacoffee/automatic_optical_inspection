clc()
import numpy as np
angle1=eval(input("angle1:"))
n1=eval(input("n1:"))
n2=eval(input("n2:"))
angle2=angle1
angle3=np.arcsin((n1/n2)*np.sin(angle1*np.pi/180))*180/np.pi

import turtle as tu
tu.pensize(5)
tu.color("blue")
tu.goto(-100,0)
tu.setheading(0)
tu.pendown()
tu.forward(200)
tu.penup()
tu.goto(0,-200)
tu.setheading(90)
tu.pendown()
tu.forward(400)
tu.penup()
tu.goto(0,0)
tu.setheading(180)
tu.left(angle1)
tu.forward(400)
tu.left(180)
tu.pendown()
tu.forward(400)
tu.left(180-2*angle1)
tu.forward(400)
tu.penup()
tu.goto(0,0)
tu.setheading(0)
tu.left(angle3)
tu.pendown()
tu.forward(400)
tu.penup()
angle1=str(angle1)
angle3=str(angle3)
n1=str(n1)
n2=str(n2)
tu.goto(-100,200)
tu.write("angle1="+angle1,False,'center',('Arial',8,'normal'))
tu.goto(100,200)
tu.write("angle2="+angle3,False,'center',('Arial',8,'normal'))
tu.goto(-100,-200)
tu.write("n1="+n1, False,'center',('Arial',8,'normal'))
tu.goto(100,-200)
tu.write("n2="+n2, False,'center',('Arial',8,'normal'))
tu.done()


